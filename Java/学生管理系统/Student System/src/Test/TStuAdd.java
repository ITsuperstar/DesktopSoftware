		if(学号.getText().equals("")||姓名.getText().equals("")||性别.getText().equals("")||生日.getText().equals("")||系别.getText().equals("")){
			JOptionPane.showMessageDialog(this,"学生信息请填满再录入！" );
		}
		Statement stmt=null;
		ResultSet rs1=null;
		String sql,sql1;
		    sql1="SELECT * FROM STUDENT WHERE SNO='"+学号.getText()+"'";
		    sql="INSERT INTO STUDENT VALUES('"+学号.getText()+"','"+姓名.getText()+"','"+性别.getText()+"','"+生日.getText()+"','"+系别.getText()+"')";
